# 零值：每一个数据类型都有一个布尔为false的值
# 整形的零值:0   字符串的零值: ""  列表的零值是空列表  字典的零值是空字典
# 内置函数：bool
print(bool(1))
print(bool(0))    # 整形下只有0的布尔值为false，其他的都是true
print(bool("false"))
print(bool(""))    # 字符串下只有空字符串为false，其他都是true

